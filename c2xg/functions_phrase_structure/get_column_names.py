#---------------------------------------------------------------------------------------------#
#INPUT: Length of current ngram window -------------------------------------------------------#
#OUTPUT: List of column names for pos ngram search -------------------------------------------#
#---------------------------------------------------------------------------------------------#
def get_column_names(length):

	column_names = []
	
	for i in range(length):
		column_names.append('Pos' + str(i+1))

	return column_names
