from numba import jit

@jit
def calculate_association(a, b, c, d, direction):

	if direction == "LR":
			
		pair_association = float(a / (a + c)) - float(b / (b + d))
				
	elif direction == "RL":
			
		pair_association = float(a / (a + b)) - float(c / (c + d))
	
	return pair_association
